/********************************************************************************** 
考pointer加法
a+1  會讓a的base address加上a的元素大小
&a+1 會讓a的base address加上a的陣列大小
**********************************************************************************/
#include <stdio.h>
#include <stdlib.h>

int main(void) {
  int a[5] = {1, 2, 3, 4, 5};
  int *p = (int *)(&a + 1);
  printf("%d %d\n", *(a + 1), *(p - 1)); // 2 5
  return 0;
}